package importData;

/**
 * 道口-平台-船舶三点之间的距离信息
 * Created by csw on 2016/1/18.
 */
public class DistanceCrossingPlatformVessel {
    private Integer CP;
    private Integer CV;
    private Integer PV;

    public Integer getCP() {
        return CP;
    }

    public void setCP(Integer CP) {
        this.CP = CP;
    }

    public Integer getCV() {
        return CV;
    }

    public void setCV(Integer CV) {
        this.CV = CV;
    }

    public Integer getPV() {
        return PV;
    }

    public void setPV(Integer PV) {
        this.PV = PV;
    }
}
