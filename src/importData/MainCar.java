package importData;

/**
 * 主小车在各个位置之间的运行速度
 * Created by csw on 2016/1/18.
 */
public class MainCar {

    private Integer CPSPEED;
    private Integer CVSPEED;
    private Integer PVSPEED;

    public Integer getCPSPEED() {
        return CPSPEED;
    }

    public void setCPSPEED(Integer CPSPEED) {
        this.CPSPEED = CPSPEED;
    }

    public Integer getCVSPEED() {
        return CVSPEED;
    }

    public void setCVSPEED(Integer CVSPEED) {
        this.CVSPEED = CVSPEED;
    }

    public Integer getPVSPEED() {
        return PVSPEED;
    }

    public void setPVSPEED(Integer PVSPEED) {
        this.PVSPEED = PVSPEED;
    }
}
